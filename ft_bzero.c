#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*bis;
	size_t			i;

	bis = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		bis[i] = 0;
		i++;
	}
}
